    private void logIn(){
        UserDto userDto = new UserDto();
        UserDao userDao = null;
                
        try {
            userDao = new UserDao();
        } catch (Exception ex) {
            Logger.getLogger(frm_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            if(userDao.LogIn(jtxtUser.getText(), jtxtPass.getText())){
               // JOptionPane.showMessageDialog(this, "Entr√≥...");
                frm_Principal principal = new frm_Principal();
                principal.setDefaultCloseOperation(WindowConstants.EXIT_ON_CLOSE);
                principal.setPreferredSize(new Dimension(400, 400));
                principal.pack();
                principal.setVisible(true);
                this.dispose();
                ;
            }else{
                JOptionPane.showMessageDialog(this, "Error...");
            }
// TODO add your handling code here:
        } catch (Exception ex) {
            Logger.getLogger(frm_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
